/*
Palabras Reservadas en JS:
- class: Define una clase en JavaScript.
- let: Declara una variable con ámbito de bloque.
- const: Declara una constante con ámbito de bloque.
- if, else: Estructuras de control condicional.
- for, while: Estructuras de control de bucles.
- function: Define una función.
- return: Retorna un valor de una función.
- break, continue: Controlan el flujo en bucles.
- switch: Define una estructura de control de selección múltiple.
- try, catch, finally: Manejo de excepciones.
- new: Crea una instancia de un objeto.
- typeof: Devuelve una cadena que indica el tipo de una variable.
- undefined: Representa el valor no definido.
- null: Representa la ausencia de valor o un objeto nulo.
- NaN: Representa un valor no numérico.
- true, false: Representan valores booleanos verdadero y falso.
*/